# integer variable
a = 1
print(a)

# string variable
b = "Hello World"
print(b)
b = 'Hello World'
print(b)

# float variable
c = 2.03
print(c)

# single quote and double quote
a = "Please don't go back"  # you can write apostrophes easily in double quote
print(a)
b = 'Please don\'t go back'  # you need to write backslash in front of backslash in single quote
print(b)
