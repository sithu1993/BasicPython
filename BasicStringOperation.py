"""

You will learn basic string operation

"""

# string length
myString = "Hello, World!"
print(len(myString))

# string output by index
myString = "Hello, World!"
print(myString.index("o"))

# string count
myString = "Hello, World"
print(myString.count("o"))

# string output by index [start,end]
myString = "Hello, World"
print(myString[2:8])


