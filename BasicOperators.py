# addition
result = 1 + 5
print(result)

# subtraction
result = 6 - 3
print(result)

# multiplication
result = 5 * 3
print(result)

# Using two multiplication symbols makes a power relationship
result = 2 ** 2
print(result)
result = 2 ** 3
print(result)

# division
result = 15 / 2
print(result)

# modulo
result = 15 % 2
print(result)

print("--- End Simple Basic Operators -----")

